/**
  * @brief This function handles EXTI line[9:5] interrupts.
  */
void EXTI9_5_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI9_5_IRQn 0 */
  uint16_t count = 0;
  ////printf("LOG: KEY_9_5 interrupt\n");
  if (__HAL_GPIO_EXTI_GET_FLAG(GPIO_PIN_8) != RESET){
    while ((HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_8) != GPIO_PIN_SET) && count < KEY_COUNTOUT){
      count++;
    }

    if (count >= KEY_COUNTOUT){
      //printf("LOG: up\n");
      keyUpFlag = TRUE;
    }
    __HAL_GPIO_EXTI_CLEAR_IT(GPIO_PIN_8);
  } else if (__HAL_GPIO_EXTI_GET_FLAG(GPIO_PIN_9) != RESET){
    while ((HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_9) != GPIO_PIN_SET) && count < KEY_COUNTOUT){
      count++;
    }

    if (count >= KEY_COUNTOUT){
      //printf("LOG: left\n");
      keyLeftFlag = TRUE;
    }
    __HAL_GPIO_EXTI_CLEAR_IT(GPIO_PIN_9);
  }
  /* USER CODE END EXTI9_5_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_8);
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_9);
  /* USER CODE BEGIN EXTI9_5_IRQn 1 */

  /* USER CODE END EXTI9_5_IRQn 1 */
}

