uint8_t RTC_IsLeapYear(int year)
{			  
	if(year%4==0) 
	{ 
		if(year%100==0) 
		{ 
			if(year%400==0)return 1;
			else return 0;   
		}else return 1;   
	}else return 0;	
}

int8_t RTC_CheckTime(int syear, int smon, int sday, int hour, int min, int sec)
{
  if (syear != -1 && (syear < 1970 || syear > 2099))
    return RTC_ERR_YEAR;
  
  
  if (smon != -1 && (smon == 0 || smon > 12))
    return RTC_ERR_MON;
  
  if (sday != -1 && sday == 0)
    return RTC_ERR_DATE;
  switch(smon){
    case -1:
      break;
    case 4:case 6: case 9:case 11:
      if (sday > 30)
        return RTC_ERR_DATE;
      break;
    case 2:
      if (RTC_IsLeapYear(syear)){
        if (sday > 29)
          return RTC_ERR_DATE;
      } else {
        if (sday > 28)
          return RTC_ERR_DATE;
      }
      break;
    default:
      if (sday > 31)
        return RTC_ERR_DATE;
  }
  
  if (hour != -1 && hour > 24)
    return RTC_ERR_HOUR;
  if (min != -1 && min > 59)
    return RTC_ERR_MIN;
  if (sec != -1 && sec > 59)
    return RTC_ERR_SEC;
  
  return RTC_OK;
}
